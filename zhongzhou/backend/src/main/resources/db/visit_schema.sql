CREATE TABLE IF NOT EXISTS zz_visit_record (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  appointment_id BIGINT UNSIGNED NOT NULL DEFAULT 0 COMMENT '预约ID(非预约来访填0)',
  reserve_type TINYINT NOT NULL DEFAULT 1 COMMENT '来访类型:1参观来访 2探访来访',
  visitor_name VARCHAR(32) NOT NULL COMMENT '来访人姓名',
  visitor_phone VARCHAR(20) NOT NULL COMMENT '来访人手机号',
  elder_name VARCHAR(32) NOT NULL COMMENT '被探访老人姓名',
  checkin_time DATETIME NOT NULL COMMENT '来访时间',
  checkout_time DATETIME DEFAULT NULL COMMENT '离院时间',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态:1在访 2已离院',
  is_deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除:0否 1是',
  created_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (id),
  KEY idx_visit_record_appointment (appointment_id),
  KEY idx_visit_record_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='来访登记表';

