    public String getElderIdNo() { return elderIdNo; }
    public void setElderIdNo(String elderIdNo) { this.elderIdNo = elderIdNo; }
    public String getBedNo() { return bedNo; }
    public void setBedNo(String bedNo) { this.bedNo = bedNo; }
